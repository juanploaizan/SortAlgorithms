public class Utils {

    // Array to be sorted in ascending order
    public static int[] ARRAY = { -2, 7, 15, -14, 0, 15, 0, 7,
            -7, -4, -13, 5, 8, -14, 12 };

    // Utility function to print the Array
    public static void printArray(int[] arr, int n) {
        for (int i = 0; i < n; i++) {
            System.out.print(arr[i] + " ");
        }
        System.out.print("\n");
    }
}
